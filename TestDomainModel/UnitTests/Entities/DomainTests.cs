        [TestMethod]
        public void IsDescendant_WhenOtherDomainIsDirectChild_ShouldReturnTrue()
        {
            // Arrange
            var parent = new Domain { Id = Guid.NewGuid(), Name = "Parent" };
            var child = new Domain { Id = Guid.NewGuid(), Name = "Child" };

            parent.Subdomains.Add(child);

            // Act
            var result = parent.IsDescendant(child);

            // Assert
            result.Should().BeTrue();
        }

        [TestMethod]
        public void IsDescendant_WhenMatchIsOnLaterSibling_ShouldReturnTrue()
        {
            // Arrange
            var parent = new Domain { Id = Guid.NewGuid(), Name = "Parent" };
            var child1 = new Domain { Id = Guid.NewGuid(), Name = "Child1" };
            var child2 = new Domain { Id = Guid.NewGuid(), Name = "Child2" };

            parent.Subdomains.Add(child1);
            parent.Subdomains.Add(child2);

            // Act
            var result = parent.IsDescendant(child2);

            // Assert
            result.Should().BeTrue();
        }

        [TestMethod]
        public void IsDescendant_WhenOtherDomainIsIndirectDescendant_ShouldReturnTrue()
        {
            // Arrange
            var parentDomain = new Domain { Id = Guid.NewGuid(), Name = "Parent" };
            var childDomain = new Domain { Id = Guid.NewGuid(), Name = "Child" };
            var grandChildDomain = new Domain { Id = Guid.NewGuid(), Name = "GrandChild" };

            parentDomain.Subdomains.Add(childDomain);
            childDomain.Subdomains.Add(grandChildDomain);

            // Act
            var result = parentDomain.IsDescendant(grandChildDomain);

            // Assert
            result.Should().BeTrue();
        }

