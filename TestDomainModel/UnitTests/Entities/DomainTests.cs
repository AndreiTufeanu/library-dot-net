        [TestMethod]
        public void Constructor_ShouldInitializeCollections()
        {
            // Act
            var domain = new Domain();

            // Assert
            domain.Subdomains.Should().NotBeNull();
            domain.Subdomains.Should().BeEmpty();
            domain.Books.Should().NotBeNull();
            domain.Books.Should().BeEmpty();
        }

        [TestMethod]
        public void Id_ShouldBeAutoGenerated()
        {
            // Arrange & Act
            var domain1 = new Domain();
            var domain2 = new Domain();

            // Assert
            domain1.Id.Should().NotBeEmpty();
            domain2.Id.Should().NotBeEmpty();
            domain1.Id.Should().NotBe(domain2.Id);
        }

        [TestMethod]
        public void ValidDomain_ShouldPassValidation()
        {
            // Arrange
            var domain = _fixture.Build<Domain>()
                .With(d => d.Name, "Science Fiction")
                .With(d => d.Description, "Fiction based on imagined future scientific or technological advances")
                .Create();

            // Act
            var validationResults = ValidateEntity(domain);

            // Assert
            validationResults.Should().BeEmpty();
        }

        [TestMethod]
        public void Name_WhenNullOrEmpty_ShouldFailValidation()
        {
            // Arrange
            var domain = _fixture.Build<Domain>()
                .Without(d => d.Name)
                .Create();

            // Act
            var validationResults = ValidateEntity(domain);

            // Assert
            validationResults.Should().Contain(vr =>
                vr.ErrorMessage.Contains("Domain name is required"));
        }

